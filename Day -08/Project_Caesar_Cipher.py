alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y','z', 'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

def chesor(plain_text, shift_amount,direction):
    cipher_text = ""
    if direction == "decode":
        shift_amount *= -1
    for char in plain_text:
        if char in alphabet:
            num_letter = alphabet.index(char)
            total_shift = num_letter + shift_amount
            cipher_text += alphabet[total_shift]
    print(f"The {direction}d text is {cipher_text}")



